class Solution {
public:
    vector<vector<int>> subsets(vector<int>& nums) {
        vector<vector<int> >ans;
        //[0,2^(l)-1]
        ans.push_back(vector<int>{});
        for(int i =1;i<pow(2,nums.size());i++){
            int temp = i;
            int j =0;
            vector<int>singleRow;
            while (temp!=0){
                if(temp%2){
                    singleRow.push_back(nums[j]);
                }
                temp/=2;
                j++;
            }
            ans.push_back(singleRow);
        }
        return ans;
    }
};
