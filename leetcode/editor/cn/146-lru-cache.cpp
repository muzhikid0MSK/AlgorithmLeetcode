    LRUCache(int capacity) {
        cap =capacity;
    }
    
    int get(int key) {
        if(directory[key]){
            return directory[key];
        }else{
            return -1;
        }
    }
    
    void put(int key, int value) {
        if(cur<cap){
            if(directory[key]){//å­˜åœ¨
                directory[key]=value;
                //modifiedRank.push_front(key);
            }else{
                directory[key]=value;
                cur++;
            }

            modifiedRank.push_front(key);
        }else{
            if(!directory[key]){
                directory[key]=value;
                modifiedRank.pop_back();
            }
        }


    }
